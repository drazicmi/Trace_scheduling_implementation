def bookkeeping_join(x, y, z):
    base = x + 1

    if x > 2:
        a = x + 2
        b = y + 3
        c = z * 2
        mid = a + b
        branch_result = mid + c
    else:
        a = x - 2
        b = y - 3
        c = z + 4
        mid = a - b
        branch_result = mid + c

    shared1 = branch_result + 10
    shared2 = base + z
    final = shared1 + shared2
    return final


