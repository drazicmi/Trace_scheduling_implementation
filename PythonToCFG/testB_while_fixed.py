def while_fixed_case(values, n, k):
    total = 0
    flag_sum = 0
    i = 0
    limit = k * 3

    while i < n:
        v = values[i]
        adj = v + 1
        sq = adj * adj
        total = total + sq
        flag_sum = flag_sum + 1
        i = i + 1

    result = total + limit
    return result


