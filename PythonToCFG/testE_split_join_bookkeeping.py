def split_join_bookkeeping_case(a, b, c, d, e, f):
    p = a + b

    if f > 1:
        q = c + d
        r = q + 1
        if e > 1:
            s = r + 1
            t = s + 1
        else:
            s = r - 1
            t = s - 1
        u = t + p
    else:
        u = p - 1

    indep = a + d
    out = u + indep
    return out


