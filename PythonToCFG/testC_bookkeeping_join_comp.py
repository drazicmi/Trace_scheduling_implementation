def bookkeeping_case(x, y, z, w):
    base = x + 1

    if x > 2:
        light = y + 1
        p = x + y
        q = z + w
        mixed = p + q
        deep = mixed * 2
        branch_result = deep + light
    else:
        p = x - y
        q = z - w
        branch_result = p - q

    shared1 = branch_result + 10
    shared2 = base + z
    final = shared1 + shared2
    return final


