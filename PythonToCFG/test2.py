def probable_loop(values, limit):
    total = 0
    bonus = 0
    i = 0
    N = len(values)

    while i < N:
        current = values[i]

        if current < limit:
            a = current + 1
            b = current + 2
            c = current * 2
            total = total + a + b
            bonus = bonus + c
        else:
            a = current - 1
            b = current - 2
            c = current + 5
            total = total - a
            bonus = bonus - b - c

        i = i + 1

    result = total + bonus
    return result


